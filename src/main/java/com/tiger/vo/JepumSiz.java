package com.tiger.vo;

public class JepumSiz {
	private String jepum, color, siz, inUser, inDate;
	private int giQty, ipgoQty, sengQty;
	
	public String getJepum() {
		return jepum;
	}
	public void setJepum(String jepum) {
		this.jepum = jepum;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getSiz() {
		return siz;
	}
	public void setSiz(String siz) {
		this.siz = siz;
	}
	public String getInUser() {
		return inUser;
	}
	public void setInUser(String inUser) {
		this.inUser = inUser;
	}
	public String getInDate() {
		return inDate;
	}
	public void setInDate(String inDate) {
		this.inDate = inDate;
	}
	public int getGiQty() {
		return giQty;
	}
	public void setGiQty(int giQty) {
		this.giQty = giQty;
	}
	public int getIpgoQty() {
		return ipgoQty;
	}
	public void setIpgoQty(int ipgoQty) {
		this.ipgoQty = ipgoQty;
	}
	public int getSengQty() {
		return sengQty;
	}
	public void setSengQty(int sengQty) {
		this.sengQty = sengQty;
	}	
}
